#include "libft.h"

void	ft_striter(char *s, void (*f) (char *))
{
	unsigned int	a;

	if (s == NULL || f == NULL)
		return ;
	a = 0;
	while (s[a] != '\0')
	{
		f(s + a);
		a++;
	}
}
